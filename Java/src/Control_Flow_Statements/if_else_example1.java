package Control_Flow_Statements;

public class if_else_example1 {

	public static void main(String[] args) {
		double dell_laptop_cost_i5 = 70000.99;
		double hp_laptop_cost_i5 = 65000.99;
		
		if (dell_laptop_cost_i5==hp_laptop_cost_i5) {
			System.out.println("Both prices are matched");
		}
		else {
			System.out.println("Both prices are not matched");
		}
	}

}
