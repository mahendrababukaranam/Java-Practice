package Control_Flow_Statements;

public class switch_example5 {

	public static void main(String[] args) {
		int a = 6;
		
		switch(a) {  
		
		default: System.out.println("Statement N");//o/p: Statement N
		case 0: System.out.println("Statement 1");break; // o/p: Statement 1
		case 1: System.out.println("Statement 2");break;
		case 2: System.out.println("Statement 3"); 
		case 3: System.out.println("Statement 4"); 
		case 4: System.out.println("Statement 5"); 
		
		}
	}

}
