package Data_Types;

public class int_data_type {
	
	public static void main(String[] args) {
		
		//Range: -2147483648 to 2147483647
		
		int a=20;//byte value range
		
		int b=128;//short value range
		
		int c=-2147483648;//int value range
		
		int d=2147483646;//int value range
		
		//int e=2147483648;//The literal 2147483648 of type int is out of range
		
		System.out.println("The value "+a+" is within the range between -2147483648 to 2147483647");
		
		System.out.println("The value "+b+" is within the range between -2147483648 to 2147483647");
		
		System.out.println("The value "+c+" is within the range between -2147483648 to 2147483647");
		
		System.out.println("The value "+d+" is within the range between -2147483648 to 2147483647");
		
		//System.out.println("The value "+e+" is not within the range between -2147483648 to 2147483647");
		
		
	}

}
