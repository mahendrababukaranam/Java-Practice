package Data_Types;

public class short_data_type {

	public static void main(String[] args) {
		
		//Range: -32768 to 32767
		
		short a=32106;
		
		short b=-32767;
		
		//short c=32768;//Type mismatch: cannot convert from int to short
		
		short d=20;
		
		System.out.println("The value "+a+" is within the range between -32768 to 32767");
		
		System.out.println("The value "+b+" is within the range between -32768 to 32767");
		
		//System.out.println("The value "+c+" value is not within the range between -32768 to 32767");
		
		System.out.println("The given byte value "+d+" is within the range between -32768 to 32767");

	}

}
