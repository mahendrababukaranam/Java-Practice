package Data_Types;

public class boolean_example {

	public static void main(String[] args) {
	
	
		//true or false only
		
		boolean b1 = true;   // valid F: boolean  R: boolean
		boolean b2 = True;   // invalid
		boolean b3 = FALSE;  // invalid
		boolean b4 = 'T';    // invalid F: char  R: boolean
		boolean b5 = "FALSE";// invalid F: string  R: boolean
		
		
		System.out.println(b1);
		System.out.println(b2);
		System.out.println(b3);
		System.out.println(b4);
		System.out.println(b5);

	}

}
