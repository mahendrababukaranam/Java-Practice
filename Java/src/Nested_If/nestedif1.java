package Nested_If;

public class nestedif1 {

	public static void main(String[] args) {
		 
		int marks=99;
		
		if(marks>35) {
			System.out.println("Passed");
		}
		if(marks>50) {
			System.out.println("Got Second Class");
		}
		if(marks>65) {
			System.out.println("Got First Class");
		}
		if(marks>90) {
			System.out.println("Got Distinction");
		}

	}

}
