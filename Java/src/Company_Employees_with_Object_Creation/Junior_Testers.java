package Company_Employees_with_Object_Creation;

public class Junior_Testers {
	
	void Praneetha() {
		System.out.println("Hi, this is Praneetha from Uppal");
	}
	
	void Bhuvanakruthi_Moshra() {
		System.out.println("Hi, this is Bhuvana from Telangana");
	}
	
	void Nagalakshmi_Goti() {
		System.out.println("Hi, this is Nagalakshmi from Miyapur");
	}
	
	void Niha() {
		System.out.println("Hi, this is Niha from Vijayawada");
	}
	
	public static void main(String[] args) {
		Junior_Testers jrt_details=new Junior_Testers();
		jrt_details.Bhuvanakruthi_Moshra();
		jrt_details.Praneetha();
		jrt_details.Nagalakshmi_Goti();
		jrt_details.Niha();
	}

}
