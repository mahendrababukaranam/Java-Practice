package Company_Employees_with_Object_Creation;

public class Tester_Trainees {
	
	void Pampana() {
		System.out.println("Hi, this is Pampana from Bangalore");	
	}
	
	void Ravina() {
		System.out.println("Hi, this is Ravina from Uttar Pradesh");
	}
	
	void Harish() {
		System.out.println("Hi, this is Harish from Rajasthan");
	}
	
	void Jaweria() {
		System.out.println("Hi, this is Jaweria from Bangalore");
	}
	
	void Sandeep() {
		System.out.println("Hi, this is Sandeep from Mumbai");
	}
	
	void Abdul_Nazma() {
		System.out.println("Hi, this is Abdul Nazma from Mumbai");
	}
	
	public static void main(String[] args) {
		Tester_Trainees tt_details=new Tester_Trainees();
		tt_details.Pampana();
		tt_details.Ravina();
		tt_details.Harish();
		tt_details.Jaweria();
		tt_details.Sandeep();
		
	}

}
