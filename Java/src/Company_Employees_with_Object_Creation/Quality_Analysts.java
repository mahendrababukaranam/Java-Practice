package Company_Employees_with_Object_Creation;

public class Quality_Analysts {
	
	void Mahendra() {
		System.out.println("Hi, this is Mahendra from Ongole");	
	}
	
	void Gopal_Kulkarni() {
		System.out.println("Hi, this is Gopal from Maharastra");
	}
	
	void Shivani() {
		System.out.println("Hi, this is Shivani from Warangal");
	}
	
	void Sruthi() {
		System.out.println("Hi, this is Sruthi from Warangal");
	}
	
	void Shahid_Shaik() {
		System.out.println("Hi, this is Shahid from Mumbai");
	}
	
	public static void main(String[] args) {
		Quality_Analysts qa_details=new Quality_Analysts();
		qa_details.Mahendra();
		qa_details.Gopal_Kulkarni();
		qa_details.Sruthi();
		qa_details.Shivani();
		qa_details.Shahid_Shaik();
	}

}
