package Company_Employees_with_Object_Creation;

public class CEO {

	void Adi() {
		System.out.println("Hi, this is Adi from Guntur");
	}
	
	void Ravi() {
		System.out.println("Hi, this is Ravi from Guntur");
	}
	
	public static void main(String[] args) {
		CEO ceo_details=new CEO();
		ceo_details.Adi();
		ceo_details.Ravi();
		
	}
	
	
}
